    public void Listar_producto(){
        List<Producto> Listado = prodAUX.Listar_producto();
        tabla = (DefaultTableModel) TablaListado.getModel();
        Object[] ob = new Object[5];
        for (int i=0; i < Listado.size(); i++) {
            ob[0] = Listado.get(i).getID_Producto();
            ob[1] = Listado.get(i).getNombre_Producto();
            ob[2] = Listado.get(i).getStock();
            ob[3] = Listado.get(i).getDescripcion();
            ob[4] = Listado.get(i).getPrecio();
            tabla.addRow(ob);
        }
        TablaListado.setModel(tabla);
    }
    
    public void Tclear(){
        for (int i = 0; i < tabla.getRowCount(); i++){
            tabla.removeRow(i);
            i = i-1;
        }
    }

