    private void Guardar_ProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Guardar_ProductoActionPerformed
        if (!"".equals(Nproducto.getText()) || !"".equals(Sproducto.getText()) || !"".equals(Dproducto.getText()) || !"".equals(Pproducto.getText())){
        prod.setNombre_Producto(Nproducto.getText());
        prod.setStock(Integer.parseInt(Sproducto.getText()));
        prod.setDescripcion(Dproducto.getText());
        prod.setPrecio(Float.parseFloat(Pproducto.getText()));
        prodAUX.registrar_producto(prod);
        JOptionPane.showMessageDialog(null, "Producto registrado exitosamente");
        }else{
            JOptionPane.showMessageDialog(null, "Error al registrar el producto");
        }
    }//GEN-LAST:event_Guardar_ProductoActionPerformed

