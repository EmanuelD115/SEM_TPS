    public void validar_ingreso(){
        String Nombre_Usuario = LNombreUsuario.getText();
        String Contraseña = String.valueOf(LPass.getPassword());
        if (!"".equals(Nombre_Usuario) || !"".equals(Contraseña)){
            lg = login.loguear_Usuario(Nombre_Usuario, Contraseña);
            if (lg.getNombre_Usuario()!= null && lg.getContraseña()!= null){
                Sistema_menu sis = new Sistema_menu();
                sis.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Verifique los datos de inicio de sesión");
            }
        }
    }

  
